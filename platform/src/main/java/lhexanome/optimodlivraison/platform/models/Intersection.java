package lhexanome.optimodlivraison.platform.models;

public class Intersection {
    private int x;
    private int y;
    private long id;

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public long getId() {
        return id;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void setId(long id) {
        this.id = id;
    }
}
